# Methods of Dictinary and Sets

# Adding Elements to set

Dict = {"Apple", "Banana", "Cherry"}

Dict.add("Orange")
print(Dict)

#Update

Dict.update(["mango", "Pomo", "JackFruit"])

print(Dict)

#Len function
print(len(Dict))

#Remove
Dict.remove("Pomo")

print(Dict)

#Discard function

Dict.discard("JackFruit")

print(Dict)

#Pop

Dict.pop()

print(Dict)

#Clear
Dict.clear()

print(Dict)

#Unioin

Set1 = {1,2,4}
Set2 = {'a','b','c'}

Set3 = Set1.union(Set2)

print(Set3)

